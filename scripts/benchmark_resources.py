#!/usr/bin/env python3
"""
Dynamic Resource Benchmark and Configuration Script.
Analyzes system resources and writes optimized parameters to a file.
"""
import os
import psutil
import json
import argparse
import subprocess

def get_system_resources():
    # RAM
    mem = psutil.virtual_memory()
    total_ram_mb = mem.total / (1024 * 1024)
    available_ram_mb = mem.available / (1024 * 1024)

    # Storage
    disk = psutil.disk_usage('/')
    total_storage_gb = disk.total / (1024**3)
    free_storage_gb = disk.free / (1024**3)

    return {
        "total_ram_mb": int(total_ram_mb),
        "available_ram_mb": int(available_ram_mb),
        "total_storage_gb": round(total_storage_gb, 2),
        "free_storage_gb": round(free_storage_gb, 2)
    }

def main():
    parser = argparse.ArgumentParser(description="Benchmark machine resources and output parameters.")
    parser.add_argument("--output", default="group_vars/all/resource_tuning.yaml", help="Path to write YAML parameters.")
    args = parser.parse_args()

    resources = get_system_resources()
    print("Detected System Resources:")
    print(f"Total RAM: {resources['total_ram_mb']} MB")
    print(f"Available RAM: {resources['available_ram_mb']} MB")
    print(f"Total Storage: {resources['total_storage_gb']} GB")
    print(f"Free Storage: {resources['free_storage_gb']} GB")

    # Dynamic calculation for pipecat-app based on available RAM.
    # Leave 2GB for OS and other critical jobs. Give half of the remaining to pipecat-app,
    # capping it at what makes sense (e.g., 4096).
    headroom_mb = 2048
    usable_mb = resources["available_ram_mb"] - headroom_mb

    if usable_mb < 512:
        recommended_pipecat_mem = 512
    else:
        # Give 20% of usable to pipecat-app as a baseline recommendation, capped at 4096
        recommended_pipecat_mem = min(4096, max(512, int(usable_mb * 0.2)))

    # For safety, make sure it's at least a chunk size of 256
    recommended_pipecat_mem = (recommended_pipecat_mem // 256) * 256

    print(f"\nCalculated pipecat_memory_mb: {recommended_pipecat_mem} MB")

    yaml_content = f"""# Dynamically generated by scripts/benchmark_resources.py
# System Resources:
# Total RAM: {resources['total_ram_mb']} MB
# Available RAM: {resources['available_ram_mb']} MB
# Free Storage: {resources['free_storage_gb']} GB

pipecat_memory_mb: {recommended_pipecat_mem}
"""

    os.makedirs(os.path.dirname(args.output), exist_ok=True)
    with open(args.output, "w") as f:
        f.write(yaml_content)

    print(f"\nWrote parameters to {args.output}")

if __name__ == "__main__":
    main()
