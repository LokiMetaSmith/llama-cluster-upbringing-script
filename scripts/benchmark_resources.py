#!/usr/bin/env python3
"""
Dynamic Resource Benchmark and Configuration Script.
Analyzes system resources and writes optimized parameters to a file.
"""
import os
import psutil
import json
import argparse
import subprocess

def get_system_resources():
    # 1. RAM Calculation
    mem = psutil.virtual_memory()
    raw_total_ram_mb = mem.total / (1024 * 1024)
    raw_available_ram_mb = mem.available / (1024 * 1024)

    cluster_services = ["nomad", "consul", "dockerd", "docker", "containerd", "pipecatapp"]
    pre_allocated_ram_mb = 0
    for proc in psutil.process_iter(['name', 'memory_info']):
        try:
            if proc.info['name'] in cluster_services:
                pre_allocated_ram_mb += proc.info['memory_info'].rss / (1024 * 1024)
        except (psutil.NoSuchProcess, psutil.AccessDenied, psutil.ZombieProcess):
            pass

    effective_available_ram_mb = raw_available_ram_mb + pre_allocated_ram_mb
    effective_total_ram_mb = raw_total_ram_mb + pre_allocated_ram_mb # Since total includes available, we might just add to available, but total RAM is physical RAM.
    # Actually, physical total RAM doesn't change, but effective available RAM is what matters.

    # 2. Storage Calculation
    disk = psutil.disk_usage('/')
    raw_total_storage_gb = disk.total / (1024**3)
    raw_free_storage_gb = disk.free / (1024**3)

    cluster_dirs = ["/opt/nomad", "/var/lib/docker", "/opt/pipecatapp"]
    pre_allocated_disk_mb = 0
    for d in cluster_dirs:
        try:
            out = subprocess.check_output(['sudo', '-n', 'du', '-sk', d], stderr=subprocess.DEVNULL)
            size_kb = int(out.split()[0])
            pre_allocated_disk_mb += size_kb / 1024
        except Exception:
            pass

    pre_allocated_disk_gb = pre_allocated_disk_mb / 1024
    effective_free_storage_gb = raw_free_storage_gb + pre_allocated_disk_gb

    return {
        "raw_total_ram_mb": int(raw_total_ram_mb),
        "raw_available_ram_mb": int(raw_available_ram_mb),
        "pre_allocated_ram_mb": int(pre_allocated_ram_mb),
        "effective_available_ram_mb": int(effective_available_ram_mb),
        "raw_total_storage_gb": round(raw_total_storage_gb, 2),
        "raw_free_storage_gb": round(raw_free_storage_gb, 2),
        "pre_allocated_disk_gb": round(pre_allocated_disk_gb, 2),
        "effective_free_storage_gb": round(effective_free_storage_gb, 2)
    }

def main():
    parser = argparse.ArgumentParser(description="Benchmark machine resources and output parameters.")
    parser.add_argument("--output", default="group_vars/all/resource_tuning.yaml", help="Path to write YAML parameters.")
    args = parser.parse_args()

    resources = get_system_resources()
    print("Detected System Resources:")
    print(f"Raw Total RAM: {resources['raw_total_ram_mb']} MB")
    print(f"Raw Available RAM: {resources['raw_available_ram_mb']} MB")
    print(f"Pre-allocated Cluster RAM: {resources['pre_allocated_ram_mb']} MB")
    print(f"Effective Available RAM: {resources['effective_available_ram_mb']} MB")
    print(f"Raw Total Storage: {resources['raw_total_storage_gb']} GB")
    print(f"Raw Free OS Storage: {resources['raw_free_storage_gb']} GB")
    print(f"Pre-allocated Cluster Storage: {resources['pre_allocated_disk_gb']} GB")
    print(f"Effective Free Storage: {resources['effective_free_storage_gb']} GB")

    # Dynamic calculation for pipecat-app based on available RAM.
    # Leave 2GB for OS and other critical jobs. Give half of the remaining to pipecat-app,
    # capping it at what makes sense (e.g., 4096).
    headroom_mb = 2048
    usable_mb = resources["effective_available_ram_mb"] - headroom_mb

    if usable_mb < 512:
        recommended_pipecat_mem = 512
    else:
        # Give 20% of usable to pipecat-app as a baseline recommendation, capped at 4096
        recommended_pipecat_mem = min(4096, max(512, int(usable_mb * 0.2)))

    # For safety, make sure it's at least a chunk size of 256
    recommended_pipecat_mem = (recommended_pipecat_mem // 256) * 256

    print(f"\nCalculated pipecat_memory_mb: {recommended_pipecat_mem} MB")

    yaml_content = f"""# Dynamically generated by scripts/benchmark_resources.py
# System Resources:
# Raw Total RAM: {resources['raw_total_ram_mb']} MB
# Raw Available RAM: {resources['raw_available_ram_mb']} MB
# Pre-allocated Cluster RAM: {resources['pre_allocated_ram_mb']} MB
# Effective Available RAM: {resources['effective_available_ram_mb']} MB
# Raw Free Storage: {resources['raw_free_storage_gb']} GB
# Pre-allocated Cluster Storage: {resources['pre_allocated_disk_gb']} GB
# Effective Free Storage: {resources['effective_free_storage_gb']} GB

pipecat_memory_mb: {recommended_pipecat_mem}
"""

    os.makedirs(os.path.dirname(args.output), exist_ok=True)
    with open(args.output, "w") as f:
        f.write(yaml_content)

    print(f"\nWrote parameters to {args.output}")

if __name__ == "__main__":
    main()
